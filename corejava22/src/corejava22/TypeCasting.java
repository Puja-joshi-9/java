package corejava22;

public class TypeCasting {
	
	/*
	 * syntax: 
	 *      data-type var = (data-type)val;
	 *      
	 * 1. Implicit type casting(Auto)
	 *     
	 *     byte-> short -> int -> long -> float -> double
	 * 
	 * 2. Explicit type casting(By force)
	 *      
	 *      double -> float -> long -> int -> short -> byte
	 */
	
	
	  public static void main(String[] args) {
		
		  byte b = 67;
		  int c = b; //implicit type casting
		  
		  double t = 8745.8973;
		  int it = (int)t; //explicit type casting.
		  
		  
		  
		  
	}

}
