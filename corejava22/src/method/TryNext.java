package method;

public class TryNext {
	
	public static void main(String[] args) {
		 
		
		
		
		String name[] = new String[3];
		float marks[] = new float[3];
		
		name[0] = "Puja";
		marks[0] = 98;
		
		name[1] = "Gayatri";
		marks[1] = 92;
		
		name[2] = "Manisha";
		marks[2] = 90;
		
		for(String x : name) {
			System.out.println(x);
			

		}
		for(float y : marks) {
			System.out.println(y);
			
		}
		
		System.out.println(name+ "         "+ marks);	
			
		}
		
		
	}


